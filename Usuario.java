package com.model;
//-------------------------------------------------------------------------------------------------
public class Usuario {
	private	 int id;
	private String login;
	private int senha;
	private int tipo;
	
//-------------------------------------------------------------------------------------------------
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public int getSenha() {
		return senha;
	}
	public void setSenha(int senha) {
		this.senha = senha;
	}
	public int getTipo() {
		return tipo;
	}
	public void setTipo(int tipo) {
		this.tipo = tipo;
	}
	
	
//-------------------------------------------------------------------------------------------------
}
